<script>
 function LPass() {
    var x = document.getElementById("password");
    var y = document.getElementById("toggle-password");

    if (x.type === "password") {
      x.type = "text";
      y.className="fas fa-eye-slash";
      y.title="Masquer mot de passe";

    } else {
      x.type = "password";
      y.className="fas fa-eye";
      y.title="Afficher mot de passe";
    }
  }</script>
